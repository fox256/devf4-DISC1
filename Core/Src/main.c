uint8_t DS1307_GetRegByte(uint8_t regAddr) {
	uint8_t val;
	HAL_I2C_Master_Transmit(&hi2c3, DS1307_I2C_ADDR << 1, &regAddr, 1, DS1307_TIMEOUT);
	HAL_I2C_Master_Receive(&hi2c3, DS1307_I2C_ADDR << 1, &val, 1, DS1307_TIMEOUT);
	return val;
}


